# Variables, Lists, Tuples, Sets, Dictionaries, Data Types
import os
os.system("cls")
print("Hello World!")
my_name = "Humberto"
print(my_name)
my_variable = "Python"
print(my_variable,type(my_variable))
my_variable1 = 10
print(my_variable1,type(my_variable1))
my_variable2 = 99.99
print(my_variable2,type(my_variable2))
my_variable3 = True
print(my_variable3,type(my_variable3))
my_list = [1,2,3,4,5]
print(my_list,type(my_list))
my_tuple = (1,2,3,4,5) ## cannot modify values
print(my_tuple,type(my_tuple))
my_set = {1,2,3,4,1,2,5,2,3,5} ## cannot have duplicates
print(my_set,type(my_set))  
my_dictionary = {"name":"Maria","age":35}
print(my_dictionary,type(my_dictionary))


